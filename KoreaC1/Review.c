#include <stdio.h>

void main0() {
	// printf("Hello World");
	printf("이민혁\n");
	printf("이민혁\n");
	printf("이민혁\n");
	
	// 변수(수학) : 변하는 수x
	// 변수(프로그래밍) : 값을 저장하는 공간
	
	// 변수 생성
	// type name = OO;
	int 숫자공간1 = 314;			// 변수를 만들고 314를 저장
	int 숫자공간2;				// 변수(공간)만 만듦
	숫자공간2 = 333;				// 숫자공간2에 333을 저장
	
	// 자료형(type) : int, char, float, ...(공간을 처음 생성할때만 한번 적어줌, 이미 만들어진 변수에 대해서는 적지 않는다)

	숫자공간1 = 100;				// 숫자공간1에 100을 저장, 이전에 있던 값을 삭제

	// 주의할 점 : =
	// = (수학) : 같다
	// = (프로그래밍) : 오른쪽에 적힌 값을 왼쪽 변수에 저장

	// 변수 사용 %d %f %s %c
	printf("\n첫번째는 %d입니다\n", 숫자공간1);	// 변수 이름을 적는다는 것은 변수안에 값을 사용하겠다는 뜻 (단, =이 있으면 사용이 아닌 저장)
	printf("첫번째는 %d입니다. 두번째는 %d입니다.\n\n", 숫자공간2, 숫자공간1);	// 첫번째 %d에는 100이, 두번째 %d에는 333이 들어감

	// 숫자를 저장하는 변수 %d
	int num1 = 3;
	// 실수를 저장하는 변수(소수점있는 숫자) %f
	float num2 = 3.14f;
	// 문자를 저장하는 변수 %c
	char c = 'A';
	// 문자열(문자들을 뭉쳐놓은 공간)를 저장하는 변수 %s
	char s[100] = "아무개";		

	printf("%s\n", s);		// %s 자리에 s[100]안에 들어있는 값이 들어감
	printf("%s\n", s);
	printf("%s\n", s);
}
